function setupDrag() {
  const doll = $("#doll");
  const stage = $("#welcome");
  let dragging = false;
  let activePointerId = null;
  let offsetX = 0;
  let offsetY = 0;
  let nextLeft = 0;
  let nextTop = 0;
  let frame = null;

  const clampToStage = (event) => {
    const stageRect = stage.getBoundingClientRect();
    const dollRect = doll.getBoundingClientRect();
    nextLeft = Math.max(8, Math.min(event.clientX - stageRect.left - offsetX, stageRect.width - dollRect.width - 8));
    nextTop = Math.max(8, Math.min(event.clientY - stageRect.top - offsetY, stageRect.height - dollRect.height - 8));
  };

  const applyDrag = () => {
    doll.style.left = `${nextLeft}px`;
    doll.style.top = `${nextTop}px`;
    frame = null;
  };

  doll.addEventListener("pointerdown", (event) => {
    if (window.matchMedia("(max-width: 760px)").matches) return;
    event.preventDefault();
    dragging = true;
    activePointerId = event.pointerId;
    doll.setPointerCapture(event.pointerId);
    doll.classList.add("dragging");
    const rect = doll.getBoundingClientRect();
    const stageRect = stage.getBoundingClientRect();
    doll.style.left = `${rect.left - stageRect.left}px`;
    doll.style.top = `${rect.top - stageRect.top}px`;
    doll.style.bottom = "auto";
    doll.style.right = "auto";
    offsetX = event.clientX - rect.left;
    offsetY = event.clientY - rect.top;
    clampToStage(event);
  });

  window.addEventListener("pointermove", (event) => {
    if (!dragging || event.pointerId !== activePointerId) return;
    event.preventDefault();
    clampToStage(event);
    if (!frame) frame = window.requestAnimationFrame(applyDrag);
  });

  const stopDrag = () => {
    if (!dragging) return;
    if (frame) {
      window.cancelAnimationFrame(frame);
      applyDrag();
    }
    dragging = false;
    activePointerId = null;
    doll.classList.remove("dragging");
  };

  window.addEventListener("pointerup", stopDrag);
  window.addEventListener("pointercancel", stopDrag);
  doll.addEventListener("lostpointercapture", stopDrag);
}

function setupContactCopy() {
  const contact = $("#contact");
  const toast = $("#contactToast");
  if (!contact || !toast) return;
  let toastTimer = 0;

  const copyText = async (value) => {
    if (navigator.clipboard?.writeText) {
      const copiedWithClipboard = await Promise.race([
        navigator.clipboard.writeText(value).then(() => true).catch(() => false),
        new Promise((resolve) => window.setTimeout(() => resolve(false), 500)),
      ]);
      if (copiedWithClipboard) return true;
    }

    const textarea = document.createElement("textarea");
    textarea.value = value;
    textarea.setAttribute("readonly", "");
    textarea.style.position = "fixed";
    textarea.style.left = "-9999px";
    textarea.style.opacity = "0";
    document.body.appendChild(textarea);
    textarea.select();
    textarea.setSelectionRange(0, value.length);
    let copied = false;
    try {
      copied = document.execCommand("copy");
    } catch (error) {
      copied = false;
    }
    textarea.remove();
    return copied;
  };

  const showToast = (message, success) => {
    window.clearTimeout(toastTimer);
    toast.textContent = message;
    toast.classList.toggle("is-error", !success);
    toast.classList.add("is-visible");
    toastTimer = window.setTimeout(() => toast.classList.remove("is-visible"), 1800);
  };

  contact.querySelectorAll("[data-copy-value]").forEach((card) => {
    card.addEventListener("click", async () => {
      const copied = await copyText(card.dataset.copyValue);
      card.classList.toggle("is-copied", copied);
      showToast(copied ? card.dataset.copySuccess : "复制失败，请手动复制", copied);
      card.focus({ preventScroll: true });
      window.setTimeout(() => card.classList.remove("is-copied"), 620);
    });
  });
}

